def mayor_de_tres(num1, num2, num3):
    """
    Esta funcion recibe tres numeros como argumentos y devuelve el mayor de ellos.
    
    Args:
    num1 (float): El primer numero.
    num2 (float): El segundo numero.
    num3 (float): El tercer numero.
    
    Returns:
    float: El mayor de los tres numeros.
    """
    return max(num1, num2, num3)

  